let multiplicar = n1 * n2
let divisão = n1 / n2
let subtração = n1 - n2

document.write("A soma é:", soma, "<br>")
document.write("A multiplicação é:", multiplicar, "<br>")
document.write("A divisão é:", divisão, "<br>")
document.write("A subtração é:", subtração, "<br>")